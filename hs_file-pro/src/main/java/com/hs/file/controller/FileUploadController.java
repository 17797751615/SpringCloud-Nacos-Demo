@RestController
@RequestMapping("/api/v1.0/file")
public class FileUploadController {
    @Autowired
    private HsFileUtils hsFileUtils;

    @PostMapping
    public String saveFile(MultipartFile file) throws IOException {
        String fileName = hsFileUtils.saveFile(file);
        return fileName;
    }
}
